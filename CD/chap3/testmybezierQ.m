tic;
x = 0 : 5 ; 
y = 5 * rand ( 1 , length(x) ) ;
figure;
hold on;
plot ( x , y , 'bx' );
[xp yp] = findbezierctrl ( x , y );
plot ( xp , yp , 'ko' );
plot ( xp , yp , 'k-' );
plotbezier ( xp , yp );
toc;
