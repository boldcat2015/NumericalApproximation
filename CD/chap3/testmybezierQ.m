t = linspace ( 0 , 1 , 12 );
t = 2 * pi * t ;
xx = cos(t);
yy = sin(t);
figure;
hold on;
plot ( xx , yy , 'bx' );
[xxp yyp] = findbezierctrl ( xx , yy );
plot ( xxp , yyp , 'ko' );
plot ( xxp , yyp , 'k-' );
plotbezier ( xxp , yyp );
toc;

